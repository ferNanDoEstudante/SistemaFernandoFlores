    public FhfClientes viewBean() {
        FhfClientes cliente = new FhfClientes();
        int codigo = Util.strToInt(jTxtCodigo.getText());
        cliente.setFhfIdClientes(codigo);
        cliente.setFhfNome(jTxtNome.getText());
        cliente.setFhfCpf(jFmtCpf.getText());
        cliente.setFhfSexo(Util.intToStr(jCboSexo.getSelectedIndex()));
        cliente.setFhfRg(jFmtRg.getText());
        cliente.setFhfEmail(jTxtEmail.getText());
        cliente.setFhfDataNascimento(Util.strToDate(jFmtDatadeNascimento.getText()));
        cliente.setFhfCep(jFmtCep.getText());
        cliente.setFhfEndereco(jTxtEndereco.getText());
        cliente.setFhfBairro(jTxtBairro.getText());
        cliente.setFhfCidade(jTxtCidade.getText());
        cliente.setFhfEstado(jTxtEstado.getText());
        cliente.setFhfCelular(jFmtCelular.getText());
        cliente.setFhfDataCadastro(Util.strToDate(jFmtDataCadastro.getText()));
        if (jChbxAtivo.isSelected() == true) {
            cliente.setFhfAtivo("S");
        } else {
            cliente.setFhfAtivo("N");
        }
        return cliente;
    }
    public void beanView(FhfClientes cliente) {
        jTxtCodigo.setText(Util.intToStr(cliente.getFhfIdClientes()));
        jTxtNome.setText(cliente.getFhfNome());
        jFmtCpf.setText(cliente.getFhfCpf());
        jCboSexo.setSelectedIndex(Util.strToInt(cliente.getFhfSexo()));
        jFmtRg.setText(cliente.getFhfRg());
        jTxtEmail.setText(cliente.getFhfEmail());
        jFmtDatadeNascimento.setText(Util.dateToStr(cliente.getFhfDataNascimento()));
        jFmtCep.setText(cliente.getFhfCep());
        jTxtEndereco.setText(cliente.getFhfEndereco());
        jTxtBairro.setText(cliente.getFhfBairro());
        jTxtCidade.setText(cliente.getFhfCidade());
        jTxtEstado.setText(cliente.getFhfEstado());
        jFmtCelular.setText(cliente.getFhfCelular());
        jFmtDataCadastro.setText(Util.dateToStr(cliente.getFhfDataCadastro()));
        if (cliente.getFhfAtivo().equals("S") == true) {
            jChbxAtivo.setSelected(true);
        } else {
            jChbxAtivo.setSelected(false);
        }
    }
    
