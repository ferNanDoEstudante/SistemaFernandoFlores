    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        if (pesquisano == false) {
            Util.mensagem("Você precisa pesquisar um usuário antes");
        } else {
                ClientesDAO clientesDAO = new ClientesDAO();
            if (Util.perguntar("Deseja Excluir?") == true) {
                clientesDAO.delete(viewBean());
                Util.limpar(jTxtNome, jTxtEmail, jFmtCpf, jFmtRg, jCboSexo, jFmtDatadeNascimento, jFmtDataCadastro, jFmtCep, jFmtCelular, jChbxAtivo, jTxtEndereco, jTxtBairro, jTxtCidade, jTxtEstado);
            }
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        if (jTxtCodigo.getText().equals("")
                || jTxtNome.getText().equals("")
                || jFmtCpf.getText().contains(" ")
                || jFmtRg.getText().contains(" ")
                || jFmtDatadeNascimento.getText().contains(" ")
                || jTxtEmail.getText().equals("")
                || jFmtCep.getText().equals("")) {

            Util.mensagem("Preencha todos os campos obrigatórios!");
            return;
        }
        ClientesDAO clientesDAO = new ClientesDAO();
        if(incluir == true){
            clientesDAO.insert(viewBean());
        } else{
            clientesDAO.update(viewBean());
        }
        
        Util.habilitar(false, jTxtCodigo, jTxtNome, jTxtEmail, jFmtCpf, jFmtRg, jCboSexo, jFmtDatadeNascimento, jFmtDataCadastro, jFmtCep, jFmtCelular, jChbxAtivo, jTxtEndereco, jBtnCancelar, jBtnConfirmar, jTxtBairro, jTxtCidade, jTxtEstado);
        Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
        pesquisano = false;
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

